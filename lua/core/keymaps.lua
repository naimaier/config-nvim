local keymap = vim.keymap

keymap.set('n', '<leader>pv', vim.cmd.Ex)

-- Telescope
local builtin = require('telescope.builtin')
vim.keymap.set('n', '<leader>ff', builtin.find_files, { desc = 'Telescope find files' }) -- fuzzy find files in project
vim.keymap.set('n', '<leader>fg', builtin.live_grep, { desc = 'Telescope live grep' }) -- grep file contents in project

-- Navigate vim panes better
